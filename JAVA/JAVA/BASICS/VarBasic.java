package JAVA.BASICS;

public class VarBasic 
{
    public static void main(String[] args) 
    {
        int a = 10;
        float b  = 10.10f;
        char c = 'A';
        String d = "ABHINAV";

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
